def get_stock_daily_trade_date(start_trade_date='20191201', limit=180):
    LOG_INFO("获取最近可以交易的日期", "start date", start_trade_date)
    sql = """
            select
                distinct trade_date
            from stock_daily
            where trade_date >=%s
            order by trade_date desc
            limit %s
        """
    cur.execute(sql, (start_trade_date, limit))
    result = cur.fetchall()
    global VALID_TRADE_DATE_LIST
    for row in result:
        VALID_TRADE_DATE_LIST.append(row[0])

    LOG_INFO('VALID_TRADE_DATE_LIST', VALID_TRADE_DATE_LIST)
    VALID_TRADE_DATE_LIST = VALID_TRADE_DATE_LIST
    LOG_INFO('VALID_TRADE_DATE_LIST 截断', VALID_TRADE_DATE_LIST)
    return VALID_TRADE_DATE_LIST


